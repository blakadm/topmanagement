                        <h3>What is OneLab</h3>
                        <div align="justify" style='text-indent:30px'>OneLab is a <strong>Network of laboratories</strong> anchored on an <strong>IT Platform</strong> to provide a referral system for testing and calibration services at a single touch point.
                        &nbspIt aims to provide customers with convenient and easy access to laboratory testing and calibration services.It envisions to broaden public access to testingservices of all DOST laboratories and Non-DOST laboratories to provide standardized
                        services and fees.It also provides a timely and accurate test and calibration results.</div>
